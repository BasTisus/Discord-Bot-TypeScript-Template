export class TempVoiceHideCommand implements Command {
    public metadata = {
        name: 'byvoicehide',
        description: 'Versteckt den tempor√§ren Voice-Channel vor anderen Nutzern',
        type: ApplicationCommandType.ChatInput,
        dmPermission: false,
    };

    public data = new SlashCommandBuilder()
        .setName('byvoicehide')
        .setDescription('Versteckt den tempor√§ren Voice-Channel vor anderen Nutzern');

    public async execute(intr: ChatInputCommandInteraction): Promise<void> {
        const tempChannelData = (tempVoiceModule as any).isInTempChannel(intr);
        if (!tempChannelData) {
            await intr.reply({
                content: '‚ùå Du bist nicht in einem tempor√§ren Voice-Channel!',
                ephemeral: true
            });
            return;
        }

        if (!(tempVoiceModule as any).isChannelOwner(intr.guildId!, (intr.member as GuildMember).voice.channel!.id, intr.user.id)) {
            await intr.reply({
                content: '‚ùå Nur der Channel-Besitzer kann die Sichtbarkeit √§ndern!',
                ephemeral: true
            });
            return;
        }

        if (!tempChannelData.isVisible) {
            await intr.reply({
                content: '‚ùå Der Channel ist bereits versteckt!',
                ephemeral: true
            });
            return;
        }

        try {
            const voiceChannel = (intr.member as GuildMember).voice.channel as VoiceChannel;
            
            // Sammle alle Permission IDs EINMAL
            const permissionIds = Array.from(voiceChannel.permissionOverwrites.cache.keys());
            
            // Bearbeite alle Permissions sequenziell
            for (const id of permissionIds) {
                if (id === tempChannelData.ownerId) {
                    // Owner-Permissions nicht √§ndern - immer sichtbar
                    continue;
                }
                
                try {
                    await voiceChannel.permissionOverwrites.edit(id, {
                        ViewChannel: false // verstecken
                    });
                    
                    // Kleine Pause zwischen Updates um Rate-Limits zu vermeiden
                    await new Promise(resolve => setTimeout(resolve, 100));
                } catch (error) {
                    Logger.warn(`Warnung: Konnte Permission f√ºr ${id} nicht setzen: ${error}`);
                }
            }

            // Update in MongoDB
            tempChannelData.isVisible = false;
            await (tempVoiceModule as any).setTempChannel(intr.guildId!, voiceChannel.id, tempChannelData);

            // Log visibility change
            await (tempVoiceModule as any).updateTempChannelActivity(intr.guildId!, voiceChannel.id, 'channel_hidden', intr.user.id);

            const embed = new EmbedBuilder()
                .setTitle('üôà Channel versteckt!')
                .setDescription('Voice-Channel ist jetzt vor anderen Nutzern versteckt!')
                .addFields(
                    { name: 'üëÅÔ∏è Sichtbarkeit', value: 'Versteckt', inline: true },
                    { name: 'üë§ Ge√§ndert von', value: `${intr.user}`, inline: true },
                    { name: 'üí° Hinweis', value: 'Nutzer k√∂nnen den Channel nicht mehr sehen, aber bereits verbundene Nutzer bleiben verbunden.', inline: false }
                )
                .setColor(0x95a5a6)
                .setTimestamp()
                .setFooter({ text: 'TempVoice ‚Ä¢ Channel Hidden ‚Ä¢ MongoDB' });

            await intr.reply({ embeds: [embed] });
        } catch (error) {
            Logger.error('Fehler beim Verstecken des Channels', error);
            await intr.reply({
                content: '‚ùå Fehler beim Verstecken des Channels!',
                ephemeral: true
            });
        }
    }
}

