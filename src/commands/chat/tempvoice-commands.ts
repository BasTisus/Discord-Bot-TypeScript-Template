// 2. /byvoicesetowner - Besitzer √§ndern
export class TempVoiceSetOwnerCommand implements Command {
    public metadata = {
        name: 'byvoicesetowner',
        description: '√Ñndert den Besitzer des tempor√§ren Voice-Channels',
        type: ApplicationCommandType.ChatInput,
        dmPermission: false,
    };

    public data = new SlashCommandBuilder()
        .setName('byvoicesetowner')
        .setDescription('√Ñndert den Besitzer des tempor√§ren Voice-Channels')
        .addUserOption(option =>
            option.setName('user')
                .setDescription('Neuer Besitzer des Channels')
                .setRequired(true));

    public async execute(intr: ChatInputCommandInteraction): Promise<void> {
        const newOwner = intr.options.getUser('user', true);
        
        const tempChannelData = (tempVoiceModule as any).isInTempChannel(intr);
        if (!tempChannelData) {
            await intr.reply({
                content: '‚ùå Du bist nicht in einem tempor√§ren Voice-Channel!',
                ephemeral: true
            });
            return;
        }

        if (!(tempVoiceModule as any).isChannelOwner(intr.guildId!, (intr.member as GuildMember).voice.channel!.id, intr.user.id)) {
            await intr.reply({
                content: '‚ùå Nur der Channel-Besitzer kann den Besitzer √§ndern!',
                ephemeral: true
            });
            return;
        }

        if (newOwner.id === tempChannelData.ownerId) {
            await intr.reply({
                content: '‚ùå Dieser Nutzer ist bereits der Besitzer!',
                ephemeral: true
            });
            return;
        }

        const newOwnerMember = intr.guild!.members.cache.get(newOwner.id);
        if (!newOwnerMember) {
            await intr.reply({
                content: '‚ùå Nutzer nicht auf diesem Server gefunden!',
                ephemeral: true
            });
            return;
        }

        // Check if new owner is in the voice channel
        const voiceChannel = (intr.member as GuildMember).voice.channel as VoiceChannel;
        if (!voiceChannel.members.has(newOwner.id)) {
            await intr.reply({
                content: '‚ùå Der neue Besitzer muss im Voice-Channel sein!',
                ephemeral: true
            });
            return;
        }

        try {
            const oldOwnerId = tempChannelData.ownerId;
            const oldOwnerName = tempChannelData.ownerName;

            // Update ownership in MongoDB
            tempChannelData.ownerId = newOwner.id;
            tempChannelData.ownerName = newOwnerMember.displayName;
            await (tempVoiceModule as any).setTempChannel(intr.guildId!, voiceChannel.id, tempChannelData);

            // Update permissions
            const textChannel = intr.guild!.channels.cache.get(tempChannelData.textChannelId) as TextChannel;
            await (tempVoiceModule as any).updateOwnerPermissions(voiceChannel, textChannel, newOwnerMember, oldOwnerId);

            // Log ownership change
            await (tempVoiceModule as any).updateTempChannelActivity(intr.guildId!, voiceChannel.id, 'ownership_transferred', newOwner.id);

            const embed = new EmbedBuilder()
                .setTitle('üëë Besitzer ge√§ndert!')
                .setDescription(`Der Besitzer wurde erfolgreich auf **${newOwnerMember.displayName}** √ºbertragen!`)
                .addFields(
                    { name: 'üë§ Neuer Besitzer', value: `${newOwnerMember}`, inline: true },
                    { name: 'üëª Vorheriger Besitzer', value: `${oldOwnerName}`, inline: true },
                    { name: 'üëë √úbertragen von', value: `${intr.user}`, inline: true }
                )
                .setColor(0x00ff00)
                .setTimestamp()
                .setFooter({ text: 'TempVoice ‚Ä¢ Ownership Transfer ‚Ä¢ MongoDB' });

            await intr.reply({ embeds: [embed] });
        } catch (error) {
            Logger.error('Fehler beim √Ñndern des Besitzers', error);
            await intr.reply({
                content: '‚ùå Fehler beim √Ñndern des Besitzers!',
                ephemeral: true
            });
        }
    }
}

